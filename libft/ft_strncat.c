#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	unsigned int c1;
	unsigned int c2;

	c1 = 0;
	c2 = 0;
	while (s1[c1] != '\0')
		c1++;
	while (s2[c2] != '\0' && c2 < n)
	{
		s1[c1] = s2[c2];
		c1++;
		c2++;
	}
	s1[c1] = '\0';
	return (s1);
}
